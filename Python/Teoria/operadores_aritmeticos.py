#Un operador es un simbolo que realizara una operacion sobre uno o mas valores.

#los operadores aritmeticos son aquellos que realizan operaciones matematicas basicas

# suma (+): Sumara los dos valores.

numberOne = 10
numberTwo = 5

sum = numberOne + numberTwo

print(f"La suma de {numberOne} + {numberTwo} es igual a {sum}")

# resta (-): Restara el segundo valor del primero.

rest = numberOne - numberTwo

print(f"La resta de {numberOne} - {numberTwo} es igual a {rest}")

# multiplicacion (*): Multiplicara los dos valores.

multiply = numberOne * numberTwo

print(f"La multiplicacion de {numberOne} * {numberTwo} es igual a {multiply}")

# division (/): Dividira el primer valor por el segundo.

division = numberOne / numberTwo

print(f"La division de {numberOne} / {numberTwo} es igual a {division}")

# division entera (//): Dividira el primer valor por el segundo y devolvera el entero.

division = numberOne // numberTwo

print(f"La division entera de {numberOne} // {numberTwo} es igual a {division}")

# modulo (%): Devolvera el residuo de la division del primer valor por el segundo.

modulo = numberOne % numberTwo

print(f"El modulo de {numberOne} % {numberTwo} es igual a {modulo}")

# Potencia (**): Elevada el primer valor al segundo.

potencia = numberOne ** numberTwo

print(f"La potencia de {numberOne} ** {numberTwo} es igual a {potencia}")